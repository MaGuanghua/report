package com.hanthink.report.pojo;

public class AdctResultDomain {

	private String dateYmd;
	private String mould;
	private String proline;
	private String shift;
	private Long totaltime;
	private Integer counter;

	public String getDateYmd() {
		return dateYmd;
	}

	public void setDateYmd(String dateYmd) {
		this.dateYmd = dateYmd;
	}

	public String getMould() {
		return mould;
	}

	public void setMould(String mould) {
		this.mould = mould;
	}

	public String getProline() {
		return proline;
	}

	public void setProline(String proline) {
		this.proline = proline;
	}

	public String getShift() {
		return shift;
	}

	public void setShift(String shift) {
		this.shift = shift;
	}

	public Long getTotaltime() {
		return totaltime;
	}

	public void setTotaltime(Long totaltime) {
		this.totaltime = totaltime;
	}

	public Integer getCounter() {
		return counter;
	}

	public void setCounter(Integer counter) {
		this.counter = counter;
	}

}
